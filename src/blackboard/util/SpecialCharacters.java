package blackboard.util;

public class SpecialCharacters {
 
	private String specialCharacter;
	private String alphaCharacter;
	
	public void setSpecialCharacter(String character){
	  this.specialCharacter=character;	
	}
	public String getSpecialCharacter(){
		return specialCharacter;
	}
	public void setAlphaCharacter(String character){
		  this.alphaCharacter=character;	
		}
		public String getAlphaCharacter(){
			return alphaCharacter;
		}
}

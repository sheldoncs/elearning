package blackboard.util;

public class MoodleLoadOptions {

	private int lecturer;
	private int student;
	private int lecturerStudent;
	public int getLecturer() {
		return lecturer;
	}
	public void setLecturer(int lecturer) {
		this.lecturer = lecturer;
	}
	public int getStudent() {
		return student;
	}
	public void setStudent(int student) {
		this.student = student;
	}
	public int getLecturerStudent() {
		return lecturerStudent;
	}
	public void setLecturerStudent(int lecturerStudent) {
		this.lecturerStudent = lecturerStudent;
	}
	
}
